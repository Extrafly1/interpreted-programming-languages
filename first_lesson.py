#               задание 2
# найти количество нечётных цифр числа, больше 3
def find(x):                            # подсчитываем количество нечётных цифр в числе
    k = 0
    while(x!=0):
        if x%10 > 3 and (x%10)%2 != 0:
            k += 1
        x = x//10
    return k
