# найти произведение таких делителей числа, сумма цифр которых меньше, чем исходного числа
def sumInt(x):                          # сумма цифр цисла
    sum = 0
    while(x!=0):
        sum += x%10
        x = x//10
    return sum

def proizIfSum(x):                      # произведение делителей удовлетворяющих условию
    z = sumInt(x)
    proiz = 1
    for i in range(1, x//2 + 1):            # диапазон от начала числа до половины
        if x%i == 0 and z > sumInt(i):
            print(" " + str(i))             # выводи удовлетворяющие цисла
            proiz *= i
    return proiz
