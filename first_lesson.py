#               задание 1
# найти сумму простых делителей числа
def prosInt(x):                         # проверяем на простоту
    for i in range(1, x//2 + 1):            # диапазон от начала числа до половины для оптимизации задачи
        if x%i == 0:
            return False
    return True

def sumProsInt(x):                          # cуммируем делители удовлетворяющие условию
    y = 0
    for i in range(1, x):
        if x%i == 0 and prosInt(i):
            y += i
    return y
